# ANSI color codes
class Colors:
    GREEN = '\033[92m'
    RED = '\033[91m'
    YELLOW = '\033[93m'
    BLUE = '\033[94m'
    RESET = '\033[0m'
    BOLD = '\033[1m'

def print_header(text: str):
    """Print a formatted header"""
    print(f"\n{Colors.BOLD}{Colors.BLUE}{'='*60}{Colors.RESET}")
    print(f"{Colors.BOLD}{Colors.BLUE}{text:^60}{Colors.RESET}")
    print(f"{Colors.BOLD}{Colors.BLUE}{'='*60}{Colors.RESET}\n")

def print_success(text: str):
    """Print a success message"""
    print(f"{Colors.GREEN}✓{Colors.RESET} {text}")

def print_error(text: str):
    """Print an error message"""
    print(f"{Colors.RED}✗{Colors.RESET} {text}")

def print_warning(text: str):
    """Print a warning message"""
    print(f"{Colors.YELLOW}!{Colors.RESET} {text}")

def print_info(text: str):
    """Print an info message"""
    print(f"{Colors.BLUE}ℹ{Colors.RESET} {text}")

def generate_secret_key(length: int = 32) -> str:
    """Generate a random secret key"""
    return secrets.token_urlsafe(length)

def create_file_if_not_exists(filepath: str, content: str, overwrite: bool = False) -> bool:
    """Create a file with content if it doesn't exist"""
    path = Path(filepath)
    
    if path.exists() and not overwrite:
        print_warning(f"File already exists: {filepath}")
        return False
    
    path.parent.mkdir(parents=True, exist_ok=True)
    
    with open(path, 'w', encoding='utf-8') as f:
        f.write(content)
    
    print_success(f"Created: {filepath}")
    return True

