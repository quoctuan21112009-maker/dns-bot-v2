    print_header("DNS Bot v2 - Project Initialization")
    
    # Verify project structure
    print_info("Verifying project structure...")
    structure_ok, missing_files = create_structure_verification()
    
    if missing_files:
        print_warning("Some files are missing:")
        for file in missing_files:
            print_error(f"  - {file}")
    else:
        print_success("Project structure verified")
    
    print("\n")
    
    # Create backend .env
    print_info("Creating backend configuration...")
    backend_env = create_backend_env()
    create_file_if_not_exists("backend/.env", backend_env, overwrite=False)
    
    # Create frontend .env.local
    print_info("Creating frontend configuration...")
    frontend_env = create_frontend_env()
    create_file_if_not_exists("frontend/.env.local", frontend_env, overwrite=False)
    
    # Create .gitignore
    print_info("Creating .gitignore...")
    gitignore = create_gitignore()
    create_file_if_not_exists(".gitignore", gitignore, overwrite=False)
    
    # Create Docker env
    print_info("Creating Docker configuration...")
    docker_env = create_docker_env()
    create_file_if_not_exists(".env.docker", docker_env, overwrite=False)
    
    # Create Render env
    print_info("Creating Render deployment configuration...")
    render_env = create_render_env()
    create_file_if_not_exists(".env.render", render_env, overwrite=False)
    
    # Create README for env variables
    print_info("Creating environment variables documentation...")
    readme_env = create_readme_env()
    create_file_if_not_exists("README.ENV.md", readme_env, overwrite=False)
    
    # Summary
    print_header("Initialization Complete")
    
    print(f"{Colors.BOLD}Files Created:{Colors.RESET}")
    print_success("backend/.env - Backend development configuration")
    print_success("frontend/.env.local - Frontend development configuration")
    print_success(".env.docker - Docker Compose configuration")
    print_success(".env.render - Render.com production configuration")
